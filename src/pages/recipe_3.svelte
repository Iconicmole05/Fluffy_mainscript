<h1>Der Erfrischer</h1>
<br>

<h3>Zutaten :</h3>

<ul>
    <li>Saft einer frischen Zitrone</li>
    <li>100g chrushed-Ice</li>
    <a href="/shop"><li>Eine Fluffygetränkedose (Zitrone oder Orange)</li></a>  
</ul>

<h3>Zubereitung :</h3>


<p>Die Zitone auspressen und den Saft in ein Glas geben, crushed-Ice bis zum Glasrand aufüllen und zum Schluss mit Fluffy aufgießen.
<br> Dieses Getränk am besten mit wenig Zucker genießen, so kommt der volle Fluffygeschmack ans Tageslicht.
<br> Zum Schluss das Ganze mit Minz- und Zitronenverbenenblätter abrunden.
<br>Dieses Getränk erfrischt mit nur wenigen Zutaten ihren Tag!</p>

