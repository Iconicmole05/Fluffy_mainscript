<p>Die Fluffy GmbH ist ein umweltbewusstes und innovatives Unternehmen mit zurzeit 34 Mitarbeitern.
    <br> Das aus der ehemaligen Altonaer-Traditionsbrauerei hervorgegangene Tochterunternehmen, hat seinen Sitz in Hamburg Altona. 
